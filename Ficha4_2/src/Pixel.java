public class Pixel {
	// =======================================================
    // ===================== ATRIBUTOS =======================
    // =======================================================
	
	private boolean Estado;

    // =======================================================
    // ============ ACESSORES e MODIFICADORES ===============
    // =======================================================

	public boolean isEstado() {
		return Estado;
	}

	public void setEstado(boolean estado) {
		Estado = estado;
	}

    // =======================================================
    // =================== CONSTRUTORES =====================
    // =======================================================

  
   
    // CONSTRUTOR DEFAULT
	

    // CONSTRUTOR COM PARÂMETROS
	public Pixel(boolean estado) {
		super();
		Estado = estado;
	}

    // =======================================================
    // =================== .COMPORTAMENTOS ==================
    // =======================================================




    // =======================================================
    // ============== MÉTODOS COMPLEMENTARES ==============
    // =======================================================

    // ----> toString()
	@Override
	public String toString() {
		return this.Estado ? "X" : "_";
	}


    // ----> equals()
}
